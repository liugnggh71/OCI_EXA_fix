COLUMN HOST_NAME FORMAT a30
SET LINESIZE 270 PAGESIZE 50
ALTER SESSION SET NLS_DATE_FORMAT = 'YYYY-MM-DD HH24:MI:SS';

SELECT 'Pdb'     LIST,
       P.NAME         PDB_NAME,
       P.OPEN_MODE,
       P.RESTRICTED,
       D.NAME         DB_NAME,
       I.INSTANCE_NAME,
       I.HOST_NAME,
       I.STARTUP_TIME,
       I.STATUS
  FROM v$pdbs P, V$DATABASE D, v$instance I
  ORDER BY PDB_NAME;


