SELECT DB_NAME, CON_ID, PDB_NAME, MAX(OP_TIMESTAMP)
  FROM CDB_PDB_HISTORY
  WHERE CON_ID > 2
  GROUP BY DB_NAME, CON_ID, PDB_NAME
  ORDER BY CON_ID;
